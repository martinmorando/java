/*
    Loops
*/

public class Plane {
    
    public static void main(String[] args) {

        
        int n = 10;

        // For loop is identical to PHPs
        for (int i=0; i<n; i++) {
            System.out.println( "For loop: " + i );
        }


        // While loop is identical to PHPs
        int i = 0;
        while (i < n) {
            System.out.println( "While loop: " + i );
            i++;
        }
        

    }

}